    private void jBtnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConcluirActionPerformed
        if (jTblDiretorio.getSelectedRow() == -1) {
            if ((!jTxDescricao.getText().isEmpty()) && (!jTxCaminho.getText().isEmpty())) {
                CadastroDiretorio novo = new CadastroDiretorio();
                novo.setDescricao(jTxDescricao.getText());
                novo.setCaminhoShape(jTxCaminho.getText());

                CadastroDiretorioDao cadastrarDiretorioDao = new CadastroDiretorioDao();
                cadastrarDiretorioDao.inserirCadDir(novo);

                this.ListarJTable();

                jTxDescricao.setText("");
                jTxCaminho.setText("");
                //jBtnProcurar.setEnabled(false);

            } else {
                JOptionPane.showMessageDialog(null, "Falta incluir a Descrição ou o Caminho!\nFavor revisar os campos antes de Cadastrar.",
                        "Novo Registro", JOptionPane.PLAIN_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Impossível cadastrar, pois este registro já existe!\nLimpe os campos e preencha corretamente antes de cadastrar!.",
                        "Novo Registro", JOptionPane.PLAIN_MESSAGE);
        }

    }//GEN-LAST:event_jBtnConcluirActionPerformed

    private void jBtnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnProcurarActionPerformed

        JFileChooser arquivo = new JFileChooser();
        arquivo.setFileFilter(new FileNameExtensionFilter("Arquivo Shape (.shp)", "shp"));
        arquivo.setAcceptAllFileFilterUsed(false);

        if (arquivo.showOpenDialog(this) != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File arquivoNome = arquivo.getSelectedFile();

        jTxCaminho.setText(arquivoNome.getPath());
        //System.out.println("Caminho: " + arquivoNome.getPath());

    }//GEN-LAST:event_jBtnProcurarActionPerformed

    private void jTblDiretorioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblDiretorioMouseClicked

        // Preencher campos de Descrição e Caminho ao clicar em alguma linha da tabela
        int linhaSelecionada = jTblDiretorio.getSelectedRow();
        jTxDescricao.setText((String) jTblDiretorio.getValueAt(linhaSelecionada, 1));
        jTxCaminho.setText((String) jTblDiretorio.getValueAt(linhaSelecionada, 2));

        jTxDescricao.setEditable(true);
        jTxCaminho.setEditable(true);
        jBtnProcurar.setEnabled(true);

    }//GEN-LAST:event_jTblDiretorioMouseClicked

