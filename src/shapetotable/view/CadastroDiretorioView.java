    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        
        if (jTblDiretorio.getSelectedRow() > -1) {

            if (JOptionPane.showConfirmDialog(null, "Realmente deseja Remover o registro selecionado?", "Remoção de Registro", JOptionPane.OK_CANCEL_OPTION,0,new ImageIcon("./Src/Icones/interrogacao.png")) == 0) {

                int linhaSelecionada = jTblDiretorio.getSelectedRow();
                CadastroDiretorio remove = new CadastroDiretorio();

                remove.setIdCaminho((int) jTblDiretorio.getValueAt(linhaSelecionada, 0));

                CadastroDiretorioDao removerDiretorioDao = new CadastroDiretorioDao();
                removerDiretorioDao.removerCadDir(remove);

                this.ListarJTable();

            }

        } else {
            JOptionPane.showMessageDialog(null, "Atenção: para remover algum registro é necessário selecionar alguma linha.",
                    "Alteração de Registro", JOptionPane.PLAIN_MESSAGE);
        }

        jTxDescricao.setText("");
        jTxCaminho.setText("");
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed

        if ((!jTxDescricao.getText().isEmpty()) && (!jTxCaminho.getText().isEmpty())) {
        //Icon figura = new ImageIcon();
            if (JOptionPane.showConfirmDialog(null, "Realmente deseja Alterar o registro selecionado?", "Alteração de Registro", JOptionPane.OK_CANCEL_OPTION,0,new ImageIcon("./Src/Icones/interrogacao.png")) == 0) {

                int linhaSelecionada = jTblDiretorio.getSelectedRow();
                CadastroDiretorio altera = new CadastroDiretorio();

                altera.setDescricao(jTxDescricao.getText());
                altera.setCaminhoShape(jTxCaminho.getText());
                altera.setIdCaminho((int) jTblDiretorio.getValueAt(linhaSelecionada, 0));

                CadastroDiretorioDao alterarDiretorioDao = new CadastroDiretorioDao();
                alterarDiretorioDao.atualizarCadDir(altera);

                this.ListarJTable();

            }

        } else {
            JOptionPane.showMessageDialog(null, "Atenção: para alterar algum registro é necessário selecionar alguma linha.\nDepois de alterar a Descrição ou o Caminho, clique em Alterar.",
                    "Alteração de Registro", JOptionPane.PLAIN_MESSAGE);
        }

        jTxDescricao.setText("");
        jTxCaminho.setText("");

    }//GEN-LAST:event_jBtnAlterarActionPerformed

