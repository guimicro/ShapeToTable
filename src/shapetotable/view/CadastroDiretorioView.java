    public void ListarJTable() {
        try {

            CadastroDiretorioDao buscarCadastros = new CadastroDiretorioDao();

            jTblDiretorio.getColumnModel().getColumn(0).setPreferredWidth(50);
            jTblDiretorio.getColumnModel().getColumn(1).setPreferredWidth(200);
            jTblDiretorio.getColumnModel().getColumn(2).setPreferredWidth(500);
            DefaultTableModel tabela = (DefaultTableModel) jTblDiretorio.getModel();

            tabela.setNumRows(0);

            for (CadastroDiretorio listar : buscarCadastros.getListarCadDir()) {
                //            System.out.println("ID: " + listar.getIdCaminho());
                tabela.addRow(new Object[]{
                    listar.getIdCaminho(),
                    listar.getDescricao(),
                    listar.getCaminhoShape()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema ao listar: " + e.getMessage());
        }

    }

