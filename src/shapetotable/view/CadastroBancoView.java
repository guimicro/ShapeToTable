    private void jBtnTestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTestarActionPerformed

        if (jTblBanco.getSelectedRow() > -1) {

            int linhaSelecionada = jTblBanco.getSelectedRow();
            String usu;
            String sen;
            String tns;
            String host;
            String sid;
            int porta;

            CadastroBancoDao buscar = new CadastroBancoDao();
            CadastroBanco registroSelecionado;//= new CadastroBanco();

            registroSelecionado = buscar.getBuscarCadBanco((int) jTblBanco.getValueAt(linhaSelecionada, 0));
            usu = registroSelecionado.getUsuario();
            sen = registroSelecionado.getSenha();
            tns = registroSelecionado.getTNS();
            host = registroSelecionado.getHost();
            sid = registroSelecionado.getSid();
            porta = registroSelecionado.getPorta();

            ConnectionDBOracle conectar = new ConnectionDBOracle();
//              ConnectionDBOracleBoneCP conectar = new ConnectionDBOracleBoneCP();
            try {
                if (!tns.isEmpty()) { // Cria parametro de conexão Oracle com TNS
                    conectar.setParametrosConexaoTNS(tns, usu, sen);
                    //System.out.println("caiu no TNS");
                }
            } catch (Exception e) {
                //System.out.println("Erro: " + e.getMessage());
            }
            try {
                if (!host.isEmpty()) { // Cria parametro de conexão Oracle com Comum
                    //System.out.println("caiu no Comum");
                    conectar.setParametrosConexaoComum(host, sid, porta, usu, sen);
                }
            } catch (Exception e) {
                //System.out.println("Erro: " + e.getMessage());
            }

            try {
                Connection conectado = conectar.getConnection();
                if (!conectado.isClosed()) { //revisar este parametro de tempo em segundos
                    JOptionPane.showMessageDialog(null, "Conexão '"+ jTxNomeConexao.getText() + "' é Valida!", "Teste de Conexão", JOptionPane.PLAIN_MESSAGE);
                }
                conectar.closeAll(conectado);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Conexão '"+ jTxNomeConexao.getText() +"' Não é Valida!", "Teste de Conexão", JOptionPane.PLAIN_MESSAGE);
                //Logger.getLogger(CadastroBancoView.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item da lista de Cadastros para testar a conexão!","Teste de Conexão",JOptionPane.PLAIN_MESSAGE);
        }


    }//GEN-LAST:event_jBtnTestarActionPerformed

