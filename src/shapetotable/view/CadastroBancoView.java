    private int SELECAO_COMUM_TNS = 0; // Quando for 1 = Comum, se for 2 = TNS

    public void controlarJRadioButtons(JRadioButton rBselecionado) { //METODO PARA CONTROLAR A ALTERNANCIA DOS JRadioButtons

        if (rBselecionado.isSelected()) {
            String selecionado = rBselecionado.getActionCommand();
            if (selecionado.contentEquals("COMUM")) {
                SELECAO_COMUM_TNS = 1;
                jRbTns.setSelected(false);
                jTaTns.setEnabled(false);
                jTxHost.setEnabled(true);
                jTxSid.setEnabled(true);
                jTxPorta.setEnabled(true);
                jTxHost.grabFocus();
            }
            if (selecionado.contentEquals("TNS")) {
                SELECAO_COMUM_TNS = 2;
                jRbComum.setSelected(false);
                jTaTns.setEnabled(true);
                jTaTns.setEditable(true);
                jTxHost.setEnabled(false);
                jTxSid.setEnabled(false);
                jTxPorta.setEnabled(false);
                jTaTns.grabFocus();
            }
            //System.out.println("SELECAO_COMUM_TNS: " + SELECAO_COMUM_TNS);
        }
    }

    public void ListarJTable() { //METODO PARA TRAZER OS DADOS PARA A TABELA
        try {

            CadastroBancoDao buscarCadastros = new CadastroBancoDao();

            jTblBanco.getColumnModel().getColumn(0).setPreferredWidth(50);
            jTblBanco.getColumnModel().getColumn(1).setPreferredWidth(250);
            jTblBanco.getColumnModel().getColumn(2).setPreferredWidth(160);
            DefaultTableModel tabela = (DefaultTableModel) jTblBanco.getModel();

            tabela.setNumRows(0);

            buscarCadastros.getListarCadBanco().forEach((listar) -> { //percorre o retorno do objeto arraylist
                tabela.addRow(new Object[]{
                    listar.getIdBanco(),
                    listar.getNomeConexao(),
                    listar.getUsuario()
                });
            });
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema ao listar: " + e.getMessage());
        }
    }

