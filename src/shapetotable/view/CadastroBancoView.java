    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed

        if (jTblBanco.getSelectedRow() > -1) {

            if (JOptionPane.showConfirmDialog(null, "Realmente deseja Remover o registro selecionado?", "Remoção de Registro", JOptionPane.OK_CANCEL_OPTION,0,new ImageIcon("./Src/Icones/interrogacao.png")) == 0) {

                int linhaSelecionada = jTblBanco.getSelectedRow();
                CadastroBanco remove = new CadastroBanco();

                remove.setIdBanco((int) jTblBanco.getValueAt(linhaSelecionada, 0));

                CadastroBancoDao removerDiretorioDao = new CadastroBancoDao();
                removerDiretorioDao.removerCadBanco(remove);

                this.ListarJTable();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Atenção: para remover um registro é necessário selecionar alguma linha.",
                    "Alteração de Registro", JOptionPane.PLAIN_MESSAGE);
        }
//
//        jTxNomeConexao.setText("");
//        jTxCaminho.setText("");
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed

        if ((!jTxNomeConexao.getText().isEmpty()) && (!jTxUsuario.getText().isEmpty()) && (!jTxSenha.getText().isEmpty())) {

            int linhaSelecionada = jTblBanco.getSelectedRow();
            CadastroBanco altera = new CadastroBanco();

            altera.setNomeConexao(jTxNomeConexao.getText());
            altera.setUsuario(jTxUsuario.getText());
            altera.setSenha(new String(jTxSenha.getPassword()).trim());

            if (SELECAO_COMUM_TNS == 1) { //ALTERA REGISTRO DE CONEXÃO COMUM 
                //System.out.println("SELECAO COMUM ou TNS: " + SELECAO_COMUM_TNS);
                if ((!jTxHost.getText().isEmpty()) && (!jTxSid.getText().isEmpty()) && (!jTxPorta.getText().isEmpty())) {
                    if (JOptionPane.showConfirmDialog(null, "Realmente deseja Alterar o registro selecionado?", "Alteração de Registro", JOptionPane.OK_CANCEL_OPTION,0,new ImageIcon("./Src/Icones/interrogacao.png")) == 0) {

                        altera.setHost(jTxHost.getText());
                        altera.setSid(jTxSid.getText().toUpperCase());
                        try {
                            altera.setPorta(Integer.valueOf(jTxPorta.getText()));
                            altera.setIdBanco((int) jTblBanco.getValueAt(linhaSelecionada, 0));
                            CadastroBancoDao alterarBancoDao = new CadastroBancoDao();
                            alterarBancoDao.atualizarCadBancoComum(altera);
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(null, "O campo PORTA suporta apenas o uso de números!",
                                    "Novo Registro", JOptionPane.PLAIN_MESSAGE);
                        }

                        this.ListarJTable();
                        jBtnLimparActionPerformed(evt);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Impossível alterar o cadastro sem incluir: Host, SID e Porta.",
                            "Alteração de Registro", JOptionPane.PLAIN_MESSAGE);
                }
            } else if (SELECAO_COMUM_TNS == 2) { //ALTERA REGISTRO DE CONEXÃO TNS
                if ((!jTaTns.getText().isEmpty())) {
                    if (JOptionPane.showConfirmDialog(null, "Realmente deseja Alterar o registro selecionado?", "Alteração de Registro", JOptionPane.OK_CANCEL_OPTION,0,new ImageIcon("./Src/Icones/interrogacao.png")) == 0) {

                        altera.setTNS(jTaTns.getText());
                        altera.setIdBanco((int) jTblBanco.getValueAt(linhaSelecionada, 0));

                        CadastroBancoDao alterarBancoDao = new CadastroBancoDao();
                        alterarBancoDao.atualizarCadBancoTNS(altera);

                        this.ListarJTable();
                        jBtnLimparActionPerformed(evt);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Impossível alterar o cadastro sem incluir: TNS.",
                            "Alteração de Registro", JOptionPane.PLAIN_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Atenção: para alterar algum registro é necessário selecionar alguma linha.\nDepois de alterar a Descrição ou o Caminho, clique em Alterar.",
                    "Alteração de Registro", JOptionPane.PLAIN_MESSAGE);
        }


    }//GEN-LAST:event_jBtnAlterarActionPerformed

