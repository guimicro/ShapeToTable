    private void jBtnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConcluirActionPerformed
        if (jTblBanco.getSelectedRow() == -1) {
            if ((!jTxNomeConexao.getText().isEmpty()) && (!jTxUsuario.getText().isEmpty()) && (!jTxSenha.getText().isEmpty())) {

                CadastroBanco novo = new CadastroBanco();
                novo.setNomeConexao(jTxNomeConexao.getText().trim());
                novo.setUsuario(jTxUsuario.getText().trim());
                novo.setSenha(new String(jTxSenha.getPassword()).trim());

                if (SELECAO_COMUM_TNS == 1) { // SE FOR COMUM
                    if ((!jTxHost.getText().isEmpty()) && (!jTxSid.getText().isEmpty()) && (!jTxPorta.getText().isEmpty())) {

                        novo.setHost(jTxHost.getText());
                        novo.setSid(jTxSid.getText().toUpperCase());
                        try {
                            novo.setPorta(Integer.valueOf(jTxPorta.getText()));
                            CadastroBancoDao cadastrarBancoDao = new CadastroBancoDao();
                            cadastrarBancoDao.inserirCadBancoComum(novo);
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(null, "O campo PORTA suporta apenas o uso de números!",
                                    "Novo Registro", JOptionPane.PLAIN_MESSAGE);
                        }

                        this.ListarJTable();
                        jBtnLimparActionPerformed(evt);
                    } else {
                        JOptionPane.showMessageDialog(null, "Impossível realizar o cadastro sem incluir: Host, SID e Porta.",
                                "Novo Registro", JOptionPane.PLAIN_MESSAGE);
                    }

                } else if (SELECAO_COMUM_TNS == 2) { // SE FOR TNS
                    if ((!jTaTns.getText().isEmpty())) {

                        novo.setTNS(jTaTns.getText().trim());

                        CadastroBancoDao cadastrarBancoDao = new CadastroBancoDao();
                        cadastrarBancoDao.inserirCadBancoTNS(novo);

                        this.ListarJTable();
                        jBtnLimparActionPerformed(evt);
                    } else {
                        JOptionPane.showMessageDialog(null, "Impossível realizar o cadastro sem incluir: TNS.",
                                "Novo Registro", JOptionPane.PLAIN_MESSAGE);
                    }

                }
            } else {
                JOptionPane.showMessageDialog(null, "Falta incluir o Nome da Conexão, Usuário ou Senha!\nFavor revisar os campos antes de Cadastrar.",
                        "Novo Registro", JOptionPane.PLAIN_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Impossível cadastrar, pois este registro já existe!\nLimpe os campos e preencha corretamente antes de cadastrar!.",
                    "Novo Registro", JOptionPane.PLAIN_MESSAGE);
        }

    }//GEN-LAST:event_jBtnConcluirActionPerformed

    private void jTblBancoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblBancoMouseClicked

        // Preencher campos de Descrição e Caminho ao clicar em alguma linha da tabela
        int linhaSelecionada = jTblBanco.getSelectedRow();
        int valorLinhaSelecionada = (int) jTblBanco.getValueAt(linhaSelecionada, 0);

        CadastroBancoDao buscarById = new CadastroBancoDao();
        String[] cadBancoLinha = new String[7];

        cadBancoLinha[0] = buscarById.getBuscarCadBanco(valorLinhaSelecionada).getNomeConexao();
        cadBancoLinha[1] = buscarById.getBuscarCadBanco(valorLinhaSelecionada).getUsuario();
        cadBancoLinha[2] = buscarById.getBuscarCadBanco(valorLinhaSelecionada).getSenha();
        cadBancoLinha[3] = buscarById.getBuscarCadBanco(valorLinhaSelecionada).getTNS();
        cadBancoLinha[4] = buscarById.getBuscarCadBanco(valorLinhaSelecionada).getHost();
        cadBancoLinha[5] = buscarById.getBuscarCadBanco(valorLinhaSelecionada).getSid();
        cadBancoLinha[6] = Integer.toString(buscarById.getBuscarCadBanco(valorLinhaSelecionada).getPorta());

        jTxNomeConexao.setText(cadBancoLinha[0]);
        jTxUsuario.setText(cadBancoLinha[1]);
        jTxSenha.setText(cadBancoLinha[2]);
        jTaTns.setText(cadBancoLinha[3]);
        jTxHost.setText(cadBancoLinha[4]);
        jTxSid.setText(cadBancoLinha[5]);
        if (cadBancoLinha[6].equals("0")) {
            jTxPorta.setText("");
        } else {
            jTxPorta.setText(cadBancoLinha[6]);
        }

        try {
            if (!cadBancoLinha[3].isEmpty()) { // Controlar RadioButton TNS
                jRbTns.setSelected(true);
                controlarJRadioButtons(jRbTns);
            }
        } catch (Exception e) {
            e.getMessage();
        }

        try {
            if (!cadBancoLinha[4].isEmpty()) { // Controlar RadioButton Comum
                jRbComum.setSelected(true);
                controlarJRadioButtons(jRbComum);
            }
        } catch (Exception e) {
            e.getMessage();
        }

        //jTxNomeConexao.setEditable(true);

    }//GEN-LAST:event_jTblBancoMouseClicked

